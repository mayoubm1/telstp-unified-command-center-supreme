    def get_report_file(self, filename: str) -> Optional[str]:
        """
        Get path to report file
        """
        try:
            file_path = os.path.join(self.reports_storage_path, filename)
            if os.path.exists(file_path):
                return file_path
            return None
        except Exception as e:
            self.logger.error(f"Error getting report file: {str(e)}")
            return None
    
    def list_generated_reports(self) -> Dict[str, Any]:
        """
        List all generated reports
        """
        try:
            reports_list = []
            
            if os.path.exists(self.reports_storage_path):
                for filename in os.listdir(self.reports_storage_path):
                    if filename.endswith('.pdf'):
                        file_path = os.path.join(self.reports_storage_path, filename)
                        reports_list.append({
                            'filename': filename,
                            'url': f"/api/reports/download/{filename}",
                            'created_at': datetime.fromtimestamp(os.path.getctime(file_path)).isoformat(),
                            'file_size': os.path.getsize(file_path),
                            'pages': self._count_pdf_pages(file_path)
                        })
            
            return {
                'success': True,
                'reports': reports_list,
                'total_count': len(reports_list)
            }
            
        except Exception as e:
            self.logger.error(f"Error listing reports: {str(e)}")
            return {
                'success': False,
                'error': f'Failed to list reports: {str(e)}'
            }
    
    def _count_pdf_pages(self, pdf_path: str) -> int:
        """
        Count pages in PDF file
        """
        try:
            import PyPDF2
            with open(pdf_path, 'rb') as file:
                pdf_reader = PyPDF2.PdfReader(file)
                return len(pdf_reader.pages)
        except:
            # Fallback estimate
            file_size = os.path.getsize(pdf_path)
            return max(1, file_size // 50000)  # Rough estimate

